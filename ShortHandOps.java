public class ShortHandOps {
    public static void main(String[] args) {
        int prev = 4;
        System.out.println("prev");
        // prev++;
        // System.out.println(prev);
        // System.out.println(prev);
        System.out.println(++prev);
        System.out.println(prev);
        
        int next = 4;
        System.out.println("next");
        // System.out.println(next);
        // next++;
        // System.out.println(next);
        System.out.println(next++);
        System.out.println(next);

    }
}
